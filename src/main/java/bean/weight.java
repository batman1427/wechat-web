package bean;

public class weight {
       int num;
       String type;
       public weight(int num,String type) {
    	   this.num=num;
    	   this.type=type;
       }
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
       
       
}
